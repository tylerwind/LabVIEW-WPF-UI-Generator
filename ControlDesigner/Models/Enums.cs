namespace ControlDesigner.Models
{
    /// <summary>
    /// 支持的控件类型
    /// </summary>
    public enum ControlType
    {
        /// <summary>
        /// 文本输入框
        /// </summary>
        TextInput,
        
        /// <summary>
        /// 数值显示框
        /// </summary>
        NumericDisplay,
        /// <summary>
        /// 下拉框
        /// </summary>
        ComboBoxInput,

        /// <summary>
        /// 滑动杆
        /// </summary>
        SliderInput,
        
        /// <summary>
        /// 按钮
        /// </summary>
        ButtonInput
    }
}
